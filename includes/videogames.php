	<h2>Commissioning Amount</h2>
	<p class=" description ">Composers are paid on a 
		per-minute basis. A typical game may require 
		anywhere from 20 to 80 minutes of music. Per-minute 
		fees range from $500 up to $2,500. Some game studios 
		will hire multiple, noncollaborating composers for a 
		single title or use existing music. Composers are 
		hired on a “work-for-hire” basis and usually do not 
		retain publishing rights. In most cases the up-front 
		fee will be the only income a composer receives as 
		video games rarely offer opportunities for royalty 
		income.</p>
